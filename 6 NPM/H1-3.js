/**
 * Semantic Versioning (SEMVER)
 * Versioning: specifying what version of a set of code this is...
 * ...so others can track if a new version has come out.
 * Just looking at the version gives us some information
 *
 * MajorChanges.MinorChanges.Patches
 * 1.7.3
 *
 * Patch: some bug fixes - your code is probably fine
 * Minor: Some new features were added - your code is probably fine
 * Major: Big changes - your code may break
 * ^1.7.3 = only update any minor or patch
 * ~1.7.3 = only update patch
 */